// Operadores Aritmetricos

let soma = 6 + 6
let Subtracao = 6 - 10
let multiplicacao = 6 * 6
let divisao = 6 / 6
let resto = 6 % 6

console.log(
  `Soma : ${soma},Subtração :  ${Subtracao}, Multiplicação : ${multiplicacao}, Divisão : ${divisao} Resto : ${resto}`
)

// incremento
soma++
console.log(` Incremento : ${soma}`)

// Decremento
Subtracao--
console.log(`Descremento : ${Subtracao}`)

// Exponencia
let explonenciação = 2 ** 3
console.log(`Exponenciação : ${explonenciação}`)

